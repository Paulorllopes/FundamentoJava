
public class arrayMultiDimensional {

	public static void main(String[] args) {
		String cadastro[][] =
			{
					
					{"Paulo", "7777",},
					{"Robson", "6666"},
					{"Nascimento", "5555"},
					
			};
		
		System.out.println(cadastro[0][0]);
		System.out.println(cadastro[0][1]);
		
		System.out.println(cadastro[1][0]);
		System.out.println(cadastro[1][1]);
		
		System.out.println(cadastro[2][0]);
		System.out.println(cadastro[2][1]);
		
		

	}

}
